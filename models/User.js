const mongoose = require('mongoose');
// Created User Schema
// - name --> type: string
// - email --> type: string
// - password --> type: string
// - date --> type: date
const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const User = mongoose.model('User', UserSchema);

module.exports = User;